    public void render(Entity entity, float f, float f1, float f2, float f3, float f4, float f5) {
        if (f3 <= -180.0f) {
            f3 += 360.0f;
        } else if (f3 >= 180.0f) {
            f3 -= 360.0f;
        }
        GlStateManager.pushMatrix();
        GlStateManager.enableBlend();
        GlStateManager.blendFunc(GlStateManager.SourceFactor.SRC_ALPHA, GlStateManager.DestFactor.ONE_MINUS_SRC_ALPHA);
        GlStateManager.scale(0.8f, 0.8f, 0.8f);
        GlStateManager.translate(0.0f, 1.05f, 0.0f);
        this.setRotationAngles(f, f1, f2, f3, f4, f5, entity);
        this.BodyMain.render(f5);
        GlStateManager.disableBlend();
        GlStateManager.disableLighting();
        GlStateManager.enableCull();
        OpenGlHelper.setLightmapTextureCoords(OpenGlHelper.lightmapTexUnit, 240.0f, 240.0f);
        this.GlowBodyMain.render(f5);
        GlStateManager.disableCull();
        GlStateManager.enableLighting();
        GlStateManager.popMatrix();
    }

    public void setRotationAngles(float f, float f1, float f2, float f3, float f4, float f5, Entity entity) {
        super.setRotationAngles(f, f1, f2, f3, f4, f5, entity);
        IShipEmotion ent = (IShipEmotion)entity;
        this.motionHumanPos(f2, ent);
    }

    private void motionHumanPos(float f2, IShipEmotion ent) {
        float angleX = MathHelper.cos(f2 * 0.08f);
        if (ent.getShipDepth(0) > 0.0) {
            GlStateManager.translate(0.0f, angleX * 0.025f + 0.025f, 0.0f);
        }
        if (ent.getIsSitting()) {
            GlStateManager.translate(0.0f, 0.12f, 0.0f);
        }
        this.GlowJaw.rotateAngleX = this.Jaw.rotateAngleX = angleX * 0.1f + 0.7f;
        this.EquipCannon01.rotateAngleX = angleX * 0.08f - 0.32f;
    }
